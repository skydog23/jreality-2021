package de.jreality.reader.vecmath;

public class TexCoord2f extends Tuple2f {

  public TexCoord2f() {
    super();
  }

  public TexCoord2f(double x, double y) {
    super(x, y);
  }

  public TexCoord2f(double[] v) {
    super(v);
  }

  public TexCoord2f(TexCoord2f v1) {
    super(v1);
  }

  public TexCoord2f(Tuple2f t1) {
    super(t1);
  }

}
